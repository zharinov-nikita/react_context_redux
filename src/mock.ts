export function generateWardrobeData(): IWardrobe {
    const numShelves = 5
    const booksPerShelf = 10
    const wardrobeData: IWardrobe = {
        ID: v4(),
        shelves: [],
    }

    for (let shelf = 1; shelf <= numShelves; shelf++) {
        const shelfData: IShelf = {
            ID: v4(),
            wardrobesID: wardrobeData.ID,
            books: [],
        }

        for (let book = 1; book <= booksPerShelf; book++) {
            const bookData: IBook = {
                ID: v4(),
                title: `Book Title ${book}`,
                author: `Author ${book}`,
                wardrobesID: wardrobeData.ID,
                shelfID: shelfData.ID,
                userID: null,
            }
            shelfData.books.push(bookData)
        }

        wardrobeData.shelves.push(shelfData)
    }

    return wardrobeData
}

